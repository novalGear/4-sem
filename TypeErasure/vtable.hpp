#pragma once

#include <string>

/**
 * @brief Таблица виртуальных функций для типа
 *
 * Содержит указатели на функции, реализующие поведение
 * конкретного типа. Общая для всех экземпляров Value
 * с одинаковым типом данных.
 */
struct ValueVTable {
    // ----- Операции -----
    std::string (*to_str)(const void*);               // преобразование в строку
    bool (*test)(const void*);                        // проверка валидности
    bool (*from_str)(void*, const std::string&);      // установка из строки
    void (*print_possible)();                         // печать возможных значений

    // ----- Управление памятью -----
    void* (*clone)(const void*);                      // глубокое копирование данных
    void (*destroy)(void*);                           // освобождение памяти

    // ----- Информация о типе -----
    const char* type_name;                            // имя типа
};

/**
 * @brief Статический держатель vtable для конкретного типа
 *
 * Шаблонный класс, который специализируется для каждого типа.
 * Содержит статическую vtable, инициализированную функциями
 * для работы с данным типом.
 */
template<typename T>
struct vtable_holder {
    // Статическая vtable — общая для всех Value с типом T
    static const ValueVTable vtable;
};

// Специализации для конкретных типов будут в отдельных файлах
